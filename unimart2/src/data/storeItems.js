export function getItemsBySection(sectionKey) {
  const ids = STORE_SECTIONS[sectionKey] ?? [];
  const idSet = new Set(ids);
  const byId = new Map(STORE_ITEMS.map((item) => [item.id, item]));

  return ids
    .map((id) => byId.get(id))
    .filter(Boolean)
    .map((item) => ({ ...item, section: sectionKey, inSection: idSet.has(item.id) }));
}
